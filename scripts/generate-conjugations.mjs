const PAST_PRONOUNS = ['я', 'я', 'ты', 'он', 'она', 'оно', 'мы', 'вы', 'они'];
const PRESENT_PRONOUNS = ['я', 'ты', 'он', 'она', 'оно', 'мы', 'вы', 'они'];
const FUTURE_PRONOUNS = ['я', 'ты', 'он', 'она', 'оно', 'мы', 'вы', 'они'];

function futureStem(future1sg) {
  const bare = future1sg.replace(/^θα\s+/, '');
  if (bare.endsWith('ω')) return bare.slice(0, -1);
  if (bare.endsWith('ώ')) return bare.slice(0, -1);
  return bare;
}

function typeAPast(past1sg, future1sg) {
  const plStem = futureStem(future1sg);
  if (past1sg.endsWith('σα')) {
    const stem = past1sg.slice(0, -1);
    return [past1sg, stem + 'ες', stem + 'ε', plStem + 'αμε', plStem + 'ατε', stem + 'αν'];
  }
  if (past1sg.endsWith('α')) {
    const stem = past1sg.slice(0, -1);
    return [past1sg, stem + 'ες', stem + 'ε', plStem + 'αμε', plStem + 'ατε', stem + 'αν'];
  }
  return null;
}

function typeAPresent(present1sg) {
  const stem = present1sg.endsWith('ω') ? present1sg.slice(0, -1) : present1sg.slice(0, -1);
  return [present1sg, stem + 'εις', stem + 'ει', stem + 'ουμε', stem + 'ετε', stem + 'ουν'];
}

function typeAFuture(future1sg) {
  const stem = futureStem(future1sg);
  return [future1sg, 'θα ' + stem + 'εις', 'θα ' + stem + 'ει', 'θα ' + stem + 'ουμε', 'θα ' + stem + 'ετε', 'θα ' + stem + 'ουν'];
}

function typeBPast(past1sg, future1sg) {
  return typeAPast(past1sg, future1sg);
}

function typeBPresent(present1sg) {
  if (present1sg.endsWith('άω')) {
    const stem = present1sg.slice(0, -2);
    return [present1sg, stem + 'άς', stem + 'ά', stem + 'άμε', stem + 'άτε', stem + 'άνε'];
  }
  if (present1sg.endsWith('ώ')) {
    const stem = present1sg.slice(0, -1);
    return [present1sg, stem + 'είς', stem + 'εί', stem + 'ούμε', stem + 'είτε', stem + 'ούν'];
  }
  return typeAPresent(present1sg);
}

function typeBFuture(future1sg) {
  return typeAFuture(future1sg);
}

/** @returns {GreekConj} */
function generateGreek(present, past, future, verbType) {
  if (GREEK_OVERRIDES[present]) return GREEK_OVERRIDES[present];

  const isTypeB = verbType?.includes('Β') || present.endsWith('άω') || (present.endsWith('ώ') && !present.endsWith('άω'));

  if (isTypeB) {
    const pastForms = typeBPast(past, future);
    if (!pastForms) throw new Error(`Cannot generate past for ${present}`);
    return {
      past: pastForms,
      present: typeBPresent(present),
      future: typeBFuture(future),
    };
  }

  const pastForms = typeAPast(past, future);
  if (!pastForms) throw new Error(`Cannot generate past for ${present}`);
  return {
    past: pastForms,
    present: typeAPresent(present),
    future: typeAFuture(future),
  };
}

function formatPastSection(greekPast, ruPast) {
  const lines = [];
  const greekIdx = [0, 0, 1, 2, 2, 2, 3, 4, 5];
  for (let i = 0; i < 9; i++) {
    lines.push(`${greekPast[greekIdx[i]]} - ${PAST_PRONOUNS[i]} ${ruPast[i]}`);
  }
  return lines;
}

function formatPersonSection(greekForms, ruForms, pronouns) {
  const lines = [];
  const greekIdx = [0, 1, 2, 2, 2, 3, 4, 5];
  for (let i = 0; i < 8; i++) {
    lines.push(`${greekForms[greekIdx[i]]} - ${pronouns[i]} ${ruForms[i]}`);
  }
  return lines;
}

function buildFormsSection(greek, ru) {
  const lines = [];
  lines.push(...formatPastSection(greek.past, ru.past));
  lines.push('');
  lines.push(...formatPersonSection(greek.present, ru.present, PRESENT_PRONOUNS));
  if (greek.presentExtra && ru.presentAlt) {
    lines.push('');
    lines.push(...formatPersonSection(greek.presentExtra, ru.presentAlt, PRESENT_PRONOUNS));
  }
  lines.push('');
  lines.push(...formatPersonSection(greek.future, ru.future, FUTURE_PRONOUNS));
  return lines.join('\n');
}

function parseVerbFile(content) {
  const sections = {};
  let current = null;
  for (const line of content.split('\n')) {
    const h = line.match(/^# (.+)$/);
    if (h) {
      current = h[1].trim();
      sections[current] = [];
    } else if (current) {
      sections[current].push(line);
    }
  }

  const baseLine = (sections['База'] || []).find((l) => l.trim() && !l.startsWith('#'))?.trim() || '';
  const colon = baseLine.indexOf(':');
  const formsPart = colon >= 0 ? baseLine.slice(colon + 1).trim() : '';
  const [past, present, future] = formsPart.split(/\s*-\s*/).map((s) => s.trim());

  const formLines = (sections['Формы'] || []).filter((l) => l.trim());
  const extraSections = Object.entries(sections)
    .filter(([k]) => k !== 'База' && k !== 'Формы')
    .map(([k, lines]) => ({ title: k, lines: lines.filter((l) => l.trim() || lines.indexOf(l) === 0) }));

  return { baseLine, past, present, future, formLines, extraSections, sections };
}

function rebuildFile(parsed, formsContent) {
  let out = `# База\n${parsed.baseLine}\n\n`;
  for (const sec of parsed.extraSections) {
    out += `# ${sec.title}\n${sec.lines.join('\n').trimEnd()}\n\n`;
  }
  out += `# Формы\n${formsContent}\n`;
  return out;
}

function countFormLines(formLines) {
  return formLines.filter((l) => l.includes(' - ')).length;
}

function findLemma(parsed) {
  return parsed.present;
}

let updated = 0;
let skipped = 0;
let errors = [];

for (const file of fs.readdirSync(VERBS_DIR).sort()) {
  if (SKIP_FILES.has(file)) {
    skipped++;
    continue;
  }

  const filePath = path.join(VERBS_DIR, file);
  const content = fs.readFileSync(filePath, 'utf-8');
  const parsed = parseVerbFile(content);

  if (countFormLines(parsed.formLines) >= FULL_FORMS_THRESHOLD) {
    skipped++;
    continue;
  }

  const lemma = findLemma(parsed);
  const verbType = parsed.extraSections.find((s) => s.title === 'Тип')?.lines[0]?.trim();

  try {
    const greek = generateGreek(lemma, parsed.past, parsed.future, verbType);
    const ru = RU_FORMS[lemma];
    if (!ru) throw new Error(`Missing Russian forms for ${lemma}`);

    const formsContent = buildFormsSection(greek, ru);
    const newContent = rebuildFile(parsed, formsContent);
    fs.writeFileSync(filePath, newContent, 'utf-8');
    updated++;
  } catch (e) {
    errors.push(`${file}: ${e.message}`);
  }
}

console.log(`Updated ${updated} files, skipped ${skipped} files`);
if (errors.length) {
  console.error('Errors:');
  for (const err of errors) console.error('  ' + err);
  process.exit(1);
}
