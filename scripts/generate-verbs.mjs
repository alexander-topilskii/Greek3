function verbFilename(present, ru) {
  const primaryRu = ru.split('/')[0].trim();
  return `${primaryRu} ${present}.md`;
}

function makeContent(present, past, future, ru, type) {
  return `# База
${ru} : ${past} - ${present} - ${future}

# Тип
${type}

# Формы
${past} - ${ru} (прошедшее)
${present} - ${ru} (настоящее)
${future} - ${ru} (будущее)
`;
}

let readme = '# Глаголы\n\n';
let created = 0;
let skipped = 0;

for (const group of groups) {
  readme += `## ${group.title}\n\n`;
  for (const [present, past, future, ru] of group.verbs) {
    const file = verbFilename(present, ru);
    const filePath = path.join(VERBS_DIR, file);

    if (fs.existsSync(filePath)) {
      readme += `[${present === 'φεύγω' ? ru : `${present} — ${ru}`}](${file})\n`;
      skipped++;
      continue;
    }

    fs.writeFileSync(filePath, makeContent(present, past, future, ru, group.title), 'utf-8');
    readme += `[${present} — ${ru}](${file})\n`;
    created++;
  }
  readme += '\n';
}

fs.writeFileSync(path.join(VERBS_DIR, 'readme.md'), readme.trim() + '\n', 'utf-8');
console.log(`Created ${created} files, skipped ${skipped} (already exist)`);
