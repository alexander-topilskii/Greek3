function formatContextSection(examples) {
  const lines = examples.map(
    ([gr, ru]) => `- **${gr}** — ${ru}`
  );
  return '\n\n# Контекст\n' + lines.join('\n') + '\n';
}

const files = fs
  .readdirSync(NOUNS_DIR)
  .filter((f) => f.endsWith('.md') && f.toLowerCase() !== 'readme.md');

let updated = 0;
let skipped = 0;
const errors = [];

for (const filename of files) {
  const filePath = path.join(NOUNS_DIR, filename);
  const content = fs.readFileSync(filePath, 'utf-8');

  if (content.includes('# Контекст')) {
    skipped++;
    continue;
  }

  const examples = EXAMPLES[filename];
  if (!examples) {
    errors.push(`No examples for: ${filename}`);
    continue;
  }

  if (examples.length !== 2) {
    errors.push(`Expected 2 examples for ${filename}, got ${examples.length}`);
    continue;
  }

  const trimmed = content.replace(/\s+$/, '');
  const section = formatContextSection(examples);
  fs.writeFileSync(filePath, trimmed + section, 'utf-8');
  updated++;
  console.log(`UPDATED: ${filename}`);
}

console.log('');
console.log(`Updated: ${updated}`);
console.log(`Skipped (already have # Контекст): ${skipped}`);
if (errors.length) {
  console.log(`Errors: ${errors.length}`);
  for (const e of errors) console.log(`  ERROR: ${e}`);
} else {
  console.log('Errors: 0');
}
