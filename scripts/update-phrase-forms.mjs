function patchPhrase(content, forms) {
  const formsBlock = `# Формы\n${forms.join('\n')}\n`;
  if (content.includes('# Формы')) {
    return content.replace(/# Формы\n[\s\S]*?(?=\n# |\n*$)/, formsBlock.trimEnd() + '\n\n');
  }
  const insertBefore = content.match(/\n# Контекст/);
  if (insertBefore) {
    return content.replace(/\n# Контекст/, `\n\n${formsBlock}\n# Контекст`);
  }
  return content.trimEnd() + `\n\n${formsBlock}`;
}

for (const [file, forms] of Object.entries(FORMS)) {
  const filePath = path.join(PHRASES_DIR, file);
  const content = fs.readFileSync(filePath, 'utf8');
  fs.writeFileSync(filePath, patchPhrase(content, forms));
  console.log('Updated', file);
}

console.log('Done:', Object.keys(FORMS).length, 'phrases');
